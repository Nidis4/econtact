Ο Πελάτης και ο Επαγγελματίας είναι ελεύθεροι να διαπραγματευτούν τους ειδικότερους όρους εκτέλεσης της Εργασίας και γενικότερα τους όρους που θα διέπουν την μεταξύ τους συμφωνία. Η e-contact δεν λαμβάνει μέρος και δεν φέρει καμία ευθύνη ως προς την διαπραγμάτευση των όρων αυτών.
<br>Δ) Οικονομικοί Όροι – Συναλλαγή<br>
Καθώς η e-contact  δεν συμμετέχει και δεν αποτελεί μέρος της συμφωνίας ή της σύμβασης την οποία συνάπτει ο Πελάτης με τον Επαγγελματία, έτσι δεν εμπλέκεται και με τη διαμόρφωση αλλά ούτε με την τήρηση των οικονομικών όρων που θα διέπουν αυτή την συμφωνία ή σύμβαση, όπως ενδεικτικά και όχι περιοριστικά, το ύψος της αμοιβής του Επαγγελματία ή τον χρόνο, τρόπο και τόπο εξόφλησής του. Είναι, επομένως, αποκλειστική ευθύνη του Πελάτη και του Επαγγελματία να συμφωνήσουν και να εκπληρώσουν από την μεριά τους τους ανωτέρω όρους.
Επομένως, γίνεται σαφές ότι η e-contact  δεν συμμετέχει στην οικονομική συναλλαγή μεταξύ Πελάτη και Επαγγελματία και ότι ο Πελάτης συναλλάσσεται απευθείας με τον Επαγγελματία.

<br><br>
<strong>4) Τήρηση της Νομοθεσίας</strong><br>
Ο Πελάτης και ο Επαγγελματίας οφείλουν να τηρούν την εκάστοτε ισχύουσα νομοθεσία και τις αρχές που επιβάλλουν η καλή πίστη και τα συναλλακτικά ήθη και να μεριμνούν ότι το περιεχόμενο της μεταξύ τους συμφωνίας ή σύμβασης αλλά και κάθε πράξη ή ενέργειά τους δεν αντίκειται στα ανωτέρω.
Ενδεικτικά και όχι περιοριστικά,Η e-contact  εφιστά την προσοχή του Πελάτη και του Επαγγελματία στα εξής:<br>

Τήρηση της Φορολογικής Νομοθεσίας: Ο Πελάτης και ο Επαγγελματίας οφείλουν να τηρούν την φορολογική νομοθεσία. Ως εκ τούτου, μεταξύ άλλων, οφείλουν να μεριμνούν για την επιβολή και απόδοση του αναλογούντος Φ.Π.Α. και για την έκδοση αποδείξεως από τον Επαγγελματία σε κάθε μεταξύ τους οικονομική συναλλαγή, σύμφωνα με τα οριζόμενα στο νόμο. Η e-contact  δεν φέρει καμία ευθύνη και αποποιείται οποιασδήποτε αξιώσεως αναφορικά με τα παραπάνω.
<br>Τήρηση της Ασφαλιστικής Νομοθεσίας: Ο Πελάτης και ο Επαγγελματίας οφείλουν να μεριμνούν για την τήρηση της ασφαλιστικής νομοθεσίας. Η e-contact  ενθαρρύνει τον Πελάτη να διαπιστώνει ότι ο Επαγγελματίας είναι ασφαλισμένος προτού αποδεχθεί την Προσφορά του και προβεί σε σύναψη συμφωνίας ή συμβάσεως μαζί του.
<br>Ωστόσο, η e-contact  υποδεικνύει στον Πελάτη και τον Επαγγελματία ότι σε περίπτωση που ο Επαγγελματίας είναι ανασφάλιστος (όπως για παράδειγμα συμβαίνει συχνά με τα άτομα που προσφέρουν υπηρεσίες καθαρισμού), ενδέχεται να υπάρχει η δυνατότητα να παρέχει νομίμως στον Πελάτη τις υπηρεσίες του, μέσω της διαδικασίας έκδοσης Εργοσήμου. Η e-contact  παροτρύνει τον Πελάτη και τον Επαγγελματία να ενημερωθούν από τους αρμόδιους φορείς για την συναλλαγή μέσω Εργοσήμου ώστε να διαπιστώσουν εάν για την συγκεκριμένη Εργασία υπάρχει η δυνατότητα συναλλαγής με την διαδικασία έκδοσης Eργοσήμου και άρα εάν μπορεί ο Επαγγελματίας να αναλάβει και εκτελέσει νομίμως την Εργασία.
Η e-contact δεν δύναται να γνωρίζει ή να εγγυηθεί ότι ο Επαγγελματίας είναι ασφαλισμένος και ως εκ τούτου δεν φέρει απολύτως καμία ευθύνη και αποποιείται οποιονδήποτε αξιώσεων από οποιαδήποτε άμεση, έμμεση ή παρεπόμενη ζημία σε περίπτωση που ο Επαγγελματίας είναι ανασφάλιστος, είτε αυτό διαπιστωθεί πριν είτε μετά την αποδοχή ή/και την εκτέλεση της Εργασίας εκ μέρους του Επαγγελματία. Για τους ανωτέρω λόγους, γίνεται σαφές ότι η e-contact  σε καμία περίπτωση δεν ευθύνεται και αποποιείται οποιασδήποτε αξίωσης σε περίπτωση ατυχήματος, τραυματισμού ή θανάτου κάποιου Επαγγελματία κατά την παροχή της Εργασίας ή κατά την μετάβαση στον τόπο όπου θα παρασχεθεί η Εργασία.

