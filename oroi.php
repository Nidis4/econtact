H e-contact έχει το δικαίωμα να αλλάξει τους Όρους Χρήσης, συμπεριλαμβανομένης της Πολιτικής Προστασίας Προσωπικών Δεδομένων, όπως αυτή ενσωματώνεται και αποτελεί ενιαίο σύνολο με τους παρόντες Όρους Χρήσης, εφόσον δημοσιεύσει αυτές τις αλλαγές στην Ιστοσελίδα και ενημερώσει για την τελευταία ημερομηνία αλλαγής στην αρχή του εγγράφου. Εάν ο Επαγγελματίας δεν συμφωνεί με την αλλαγή των Όρων Χρήσης, θα πρέπει να σταματήσει να χρησιμοποιεί την Ιστοσελίδα της e-contact και τις Υπηρεσίες της. Εάν εξακολουθεί να χρησιμοποιεί την Ιστοσελίδα της e-contact  και τις Υπηρεσίες της, μετά τις αλλαγές, αυτό σημαίνει πως τις αποδέχεται.
Η e-contact  έχει το δικαίωμα να χρησιμοποιεί και τρίτα πρόσωπα της αρεσκείας της για την εκπλήρωση των με την παρούσα απορρεόντων υποχρεώσεων της.
